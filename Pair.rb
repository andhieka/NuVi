class Pair
  
  def initialize(head, tail)
    @head = head
    @tail = tail
  end
  
  # Getter methods
  
  def get_head()
    return @head
  end
  
  def get_tail()
    return @tail
  end
  
  # Setter methods
  
  def set_head(head)
    @head = head
  end
  
  def set_tail(tail)
    @tail = tail
  end
  
end